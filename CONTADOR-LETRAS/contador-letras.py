# Paso 1: Pedir al usuario que ingrese una palabra
palabra_ingresada = input("Ingrese una palabra: ")

# Paso 2: Contar la cantidad de letras en la palabra ingresada
cantidad_letras = len(palabra_ingresada)

# Paso 3: Mostrar al usuario la cantidad de letras que tiene la palabra ingresada
print("La palabra", palabra_ingresada, "tiene:", cantidad_letras, "letras")